void CCommTestDlg::OnBtnBatch() 
{
	char pDirName[200];
	char pPathName[200];
	char strSub[10];
	char str[200];
	HANDLE hSearch;
	bool bSearch;
	short result;
	WORD uLen;
	DWORD lID;
	WIN32_FIND_DATA fd;
	DWORD tMatch;
	BYTE byXor;
	CFile f;
	char ch;
	int  i;
	
	GetModuleFileName (NULL, pDirName, 200);
	for (i = strlen (pDirName) - 1; i >= 0; i --)
	{
		if (pDirName[i] == '\\') break;
	}
	pDirName[i + 1] = 0;

	strcpy (pPathName, pDirName);
	strcat (pPathName, "*.fea");
	
	gnFeaCnt = 0;
	hSearch = FindFirstFile (pPathName, &fd);
	if ((DWORD)hSearch == 0xFFFFFFFF)
	{
		MessageBox ("无特征值文件!");
		return;
	}
	
	while (1)
	{
		if (gnFeaCnt <= 1000)
		{
			strcpy (pPathName, pDirName);
			strcat (pPathName, fd.cFileName);

			//取ID号
			uLen = strlen (fd.cFileName);
			
			lID = 0;
			for (i = 0; i < uLen - 4; i++)
			{
				ch = fd.cFileName[i];
				if (ch < '0' || ch > '9') break;

				lID *= 10;
				lID += ch - '0';
			}
			if (i < uLen - 4 || lID >= 0x3FFF)
			{
				MessageBox ("文件名" + CString(fd.cFileName) + "无效!");
				return;
			}
			gpIDBuf[gnFeaCnt] = (lID << 16) + gnFeaCnt;

			//取特征值
			if (f.Open (pPathName, CFile::modeRead) == NULL)
			{
				MessageBox ("文件打开失败!");
				return;
			}
			if (f.Read (gpFeaBuf[gnFeaCnt], FEA_LEN) != FEA_LEN)
			{
				f.Close();
				MessageBox ("文件读取失败!");
				return;
			}
			f.Close();
			gnFeaCnt ++;
		}
		bSearch = FindNextFile (hSearch, &fd);
		if (!bSearch) break;
	}

	sort_DWORD (gpIDBuf, gnFeaCnt);

	gnFeaIndx = 0;

	gbRegStart = true;
	UpdateData (true);
	m_nType = RADIO_FROM_VALUE_DB;
	m_nBatchCnt = gnFeaCnt;
	m_nBatchIndx = gnFeaIndx + 1;
	UpdateData (false);
	CmdRefresh();	

	gbBatch = TRUE;
	OnCmdInter();
	gnLenAck = 0;	
}

