void sort_DWORD (DWORD *e1, WORD s)
{
	int  i, j;
	DWORD p;
	j = s;
	do
	{
		for (i = 1; i <= j - 1; i++)
		{
			if (*(e1 + i - 1) > *(e1 + i))
			{
				p = *(e1 + i);
				*(e1 + i) = *(e1 + i - 1);
				*(e1 + i - 1) = p;
			}
		}
		j--;
	}while(j > 1);
}

