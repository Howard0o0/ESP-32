bool CCommTestDlg::ReadFeature()
{
	//open feature data file
	CFile f;

	UpdateData (true);
	
	if (m_strFeaPath.GetLength() == 0)
	{
		AfxMessageBox("请选择特征值文件");
		return false;
	}
	if (f.Open(m_strFeaPath, CFile::modeRead) == NULL)
	{
		AfxMessageBox ("无法打开 " + m_strFeaPath);
		return false;
	}
	//read data from file
	if (f.Read(&gpCmdDataBuf[12], sizeof(BYTE) * FEA_LEN) != sizeof(BYTE) * FEA_LEN)
	{		
		AfxMessageBox ("读 " + m_strFeaPath +" 文件错误");
		f.Close();
		return false;
	}
	f.Close();	
	if (m_b2FP)
	{
		if (f.Open(m_strFeaPath + ".fea", CFile::modeRead) == NULL)
		{
			AfxMessageBox("无法打开 " + m_strFeaPath + ".fea");
			return false;
		}
		if (f.Read(&gpCmdDataBuf[12 + FEA_LEN], sizeof(BYTE) * FEA_LEN) != sizeof(BYTE) * FEA_LEN)
		{		
			AfxMessageBox ("读 " + m_strFeaPath + ".fea" + " 文件错误");
			f.Close();
			return false;
		}
		f.Close();	
	}

	CmdRefresh();	
	return true;
}

