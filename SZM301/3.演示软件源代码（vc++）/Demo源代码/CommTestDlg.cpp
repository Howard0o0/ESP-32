void CCommTestDlg::UnlockEncode(BYTE *pSrc, BYTE *pDst, WORD nLenByte)
{
	int i, j;
	BYTE byData;

	for (i = 0; i < nLenByte; i++)
	{
		byData = pSrc[i];
		for (j = 0; j < 8; j++)
		{
			if ((byData & 0x01) != 0)
			{
				pDst[8 * i + j] = 0xFF;
			}
			else
			{
				pDst[8 * i + j] = 0;
			}
			byData >>= 1;
		}
	}
}
void CCommTestDlg::UnlockDecode(BYTE *pSrc, BYTE *pDst, WORD nLenByte)
{
	int i, j;
	BYTE byData;

	for (i = 0; i < nLenByte; i++)
	{
		byData = 0;
		for (j = 0; j < 8; j++)
		{
			byData >>= 1;
			if ((pSrc[8 * i + j] & 0x10) != 0)
			{				
				byData += 0x80;
			}
		}
		pDst[i] = byData;
	}
}



