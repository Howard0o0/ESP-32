void CCommTestDlg::OnCmdInter()
{
	int nStart = -1;
	BYTE byXor;
	int  i;
	
	if (gbBatch)
	{
		gpCmdDataBuf[9] = (gpIDBuf[gnFeaIndx] >> 16) >> 8;
		gpCmdDataBuf[10] = (gpIDBuf[gnFeaIndx] >> 16) & 0xFF;
		gpCmdDataBuf[11] = 0x01;
		memcpy (gpCmdDataBuf + 12, gpFeaBuf[gpIDBuf[gnFeaIndx] & 0xFFFF], FEA_LEN);
	}
	else
	{
		switch (gpCmdDataBuf[1])
		{
		case CMD_REG_START_DB:
			nStart = 2; 
			break;
		case CMD_FROM_VALUE_DB:
			nStart = 9; 
			break;		
		}
		if (nStart >= 0)
		{
			UpdateData (true);	
			if (gpCmdDataBuf[nStart] == 0 && gpCmdDataBuf[nStart + 1] == 0)
			{
				if (gpCmdDataBuf[1] != CMD_REG_START_DB || (!m_bUpload))			
				{
					MessageBox("用户号不能为0");
					return;
				}
			}
			if (gpCmdDataBuf[nStart + 2] == 0)
			{
				if (gpCmdDataBuf[1] != CMD_REG_START_DB || (!m_bUpload))
				{
					MessageBox("权限无效");
					return;
				}
			}
		}
		
		if (m_bFea)
		{
			if (!ReadFeature()) return;
		}
	}

	//Update the Second part of gpCmdDataBuf
	if (gnLenCmd > 8)
	{
		gpCmdDataBuf[8] = CMD_HEAD;
		gpCmdDataBuf[gnLenCmd - 1] = CMD_TAIL;
		byXor = 0;
		for (i = 9; i < gnLenCmd - 2; i++)
		{
			byXor ^= gpCmdDataBuf[i];
		}
		gpCmdDataBuf[gnLenCmd - 2] = byXor;
	}

	TXCommand (gpCmdDataBuf, gnLenCmd);
	m_editAckInfo.SetWindowText("");
	m_editAckCode.SetWindowText("");
//	m_btnCmd.EnableWindow(false);
	gnLenAck = 0;	
}

