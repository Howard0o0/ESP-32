WORD CCommTestDlg::GetParm(int nIndx)
{
	int i;
	WORD  wParm;
	char  chVal;
	char  pBuf[10];
	switch (nIndx)
	{
	case 1:
		m_editParm1.GetWindowText(pBuf, 10);
		if (strlen(pBuf) > 2) pBuf[2] = '\0';
		break;
	case 2:
		m_editParm2.GetWindowText(pBuf, 10);
		if (strlen(pBuf) > 2) pBuf[2] = '\0';
		break;
	case 3:
		m_editParm3.GetWindowText(pBuf, 10);
		if (strlen(pBuf) > 2) pBuf[2] = '\0';
		break;
	case 4:
	default:
		m_editParm12.GetWindowText(pBuf, 10);
		if (strlen(pBuf) > 4) pBuf[4] = '\0';
		break;
	}

	wParm = 0;
	for (i = 0; i < int(strlen(pBuf)); i++)
	{
		wParm *= 16;	
		chVal = pBuf[i];
		if (isupper(chVal))
		{
			wParm += chVal - 'A' + 10;
		}
		else if(islower(chVal))
		{
			wParm += chVal-'a'+10;
		}
		else
		{
			wParm += chVal-'0';
		}		
	}	

	return wParm;
}

